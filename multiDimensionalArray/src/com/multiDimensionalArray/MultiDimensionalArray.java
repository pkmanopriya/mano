package com.multiDimensionalArray;

public class MultiDimensionalArray {
       public static void main(String[] args) {
    	     int arr1[][]= {{3,4,5},{6,7,8},{9,10,11}};
    	     for (int a[]:arr1) {
    	    	    for (int j:a) {
    	    	    	System.out.println(j+" ");
    	    	    }
    	    	        System.out.println();
    	     }
       }
}

